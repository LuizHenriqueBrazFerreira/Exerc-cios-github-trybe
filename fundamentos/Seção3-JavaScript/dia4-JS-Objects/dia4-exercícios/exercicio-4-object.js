const count = {};

for (let index = 0; index < basket.length; index += 1){
  let fruit = basket[index];
  if(!count[fruit]) {
    count[fruit] = 1;
  }else {
    count[fruit] += 1;
  };
};

const newFruitBasket = [];

const incomes = Object.entries(count);

for (let i = 0; i < incomes.length; i += 1){
  if(incomes[i][1] > 1){
    newFruitBasket.push(`${incomes[i][1]} ${incomes[i][0]}s`);
  }else {
    newFruitBasket.push(`${incomes[i][1]} ${incomes[i][0]}}`);
  };
};

